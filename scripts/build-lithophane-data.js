'use strict'

// Image -> lithophane heightmap preprocessor.
//
//   node scripts/build-lithophane-data.js <input-image> [width-px]
//
// Uses ImageMagick (`convert`) to downsample + grayscale the input to a
// binary PGM (P5) at the requested width, then parses the PGM with a
// zero-dep reader and writes examples/lib/litho_heightmap.js as a
// committed JS module. The .jscad file is sandboxed (no fs access at
// render time), so the heightmap MUST be embedded — that's why we
// commit the generated data file.

const fs   = require('node:fs')
const path = require('node:path')
const cp   = require('node:child_process')

const [,, inputImage, widthArg = '120'] = process.argv

if (!inputImage) {
  console.error('usage: node scripts/build-lithophane-data.js <input-image> [width-px]')
  process.exit(2)
}

const targetWidth = parseInt(widthArg, 10)
if (!Number.isFinite(targetWidth) || targetWidth < 16 || targetWidth > 400) {
  console.error('width must be an integer between 16 and 400 (got ' + widthArg + ')')
  process.exit(2)
}

const repoRoot = path.resolve(__dirname, '..')
const outFile  = path.join(repoRoot, 'examples', 'lib', 'litho_heightmap.js')
const tmpPgm   = path.join(require('node:os').tmpdir(), `litho_${process.pid}.pgm`)

try {
  cp.execFileSync('convert', [
    inputImage,
    '-colorspace', 'Gray',
    '-resize', `${targetWidth}x`,
    '-depth', '8',
    tmpPgm
  ], { stdio: ['ignore', 'inherit', 'inherit'] })
} catch (err) {
  console.error('ImageMagick `convert` failed. Is ImageMagick installed?')
  process.exit(1)
}

const buf = fs.readFileSync(tmpPgm)

// PGM P5 (binary): "P5\n[# comment\n]*W H\n255\n<binary bytes>"
let p = 0
const isWhitespace = (b) => b === 0x20 || b === 0x0a || b === 0x09 || b === 0x0d
const readToken = () => {
  while (p < buf.length && isWhitespace(buf[p])) p++
  if (buf[p] === 0x23) {  // '#' comment
    while (p < buf.length && buf[p] !== 0x0a) p++
    return readToken()
  }
  const start = p
  while (p < buf.length && !isWhitespace(buf[p])) p++
  return buf.slice(start, p).toString('ascii')
}

const magic = readToken()
if (magic !== 'P5') {
  throw new Error(`expected binary PGM "P5" header, got "${magic}"`)
}
const W      = parseInt(readToken(), 10)
const H      = parseInt(readToken(), 10)
const maxval = parseInt(readToken(), 10)
if (!(W > 0 && H > 0 && maxval === 255)) {
  throw new Error(`bad PGM header: W=${W} H=${H} maxval=${maxval}`)
}
p++  // single whitespace separator after maxval (per PGM spec)
const values = Array.from(buf.slice(p, p + W * H))
if (values.length !== W * H) {
  throw new Error(`pixel byte count mismatch: expected ${W*H}, got ${values.length}`)
}

fs.unlinkSync(tmpPgm)

// Emit module.exports on a single line of identifiers only; the bundler's
// regex chokes on commas inside multi-line object literals (it would treat
// each value-array element as a destructure name).
const body = `'use strict'
// AUTO-GENERATED by scripts/build-lithophane-data.js — do not edit by hand.
// Source: ${path.basename(inputImage)} (downsampled to ${W}x${H} grayscale)
// Re-run the script with your own image to replace this data.
const w = ${W}
const h = ${H}
const source = ${JSON.stringify(path.basename(inputImage))}
const values = [${values.join(',')}]
module.exports = { w, h, source, values }
`

fs.writeFileSync(outFile, body)
console.log(`Wrote ${path.relative(repoRoot, outFile)} (${W}×${H} = ${values.length} grayscale samples)`)
